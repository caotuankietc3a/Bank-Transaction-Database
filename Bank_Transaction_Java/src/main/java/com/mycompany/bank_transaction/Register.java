        int filestate = chooser.showSaveDialog(null);
        
        if(filestate == JFileChooser.APPROVE_OPTION){
            
            File selectI = chooser.getSelectedFile();
            PATH = selectI.getAbsolutePath();
            path.setText(PATH);
            
            imageP = PATH;
        }
